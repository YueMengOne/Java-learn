package com.yuemeng.compareoperatorDemo;

public class CompareOperatorDemo01 {
    public static void main(String[] args) {
        // == 判断两边的值是否相等
        int a = 10;
        int b = 20;
        int c = 10;

        System.out.println(a == b);
        System.out.println(a == c);

        System.out.println("-------------------------");

        // != 判断两边的值是否不相等
    }
}
