package com.yuemeng.Variable;

public class ab_VariableTest03 {
    // 主入口
    public static void main(String[] args) {
        // 定义变量记录电影的名字
        String name = "《唐顿庄园》";
        // 定义变量记录主演的名字
        String tostar = "张艺兴";
        // 定义变量记录电影的年份
        int year = 2017;
        // 定义变量记录电影评分
        double grade = 9.5;

        // 输出
        System.out.println("电影名称：" + name);
        System.out.println("主演：" + tostar);
        System.out.println("上映年份：" + year);
        System.out.println("评分：" + grade);
    }
}
