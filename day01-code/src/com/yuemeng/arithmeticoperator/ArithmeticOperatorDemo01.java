package com.yuemeng.arithmeticoperator;

public class ArithmeticOperatorDemo01 {
    public static void main(String[] args) {
        // +
        System.out.println(5 + 3);
        // -
        System.out.println(5 - 3);

        //*
        System.out.println(5 * 3);
        // /
        System.out.println(5 / 3);
        System.out.println(5.0 / 3);

        // %
        System.out.println(5 % 3);
    }
}
